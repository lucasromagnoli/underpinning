CREATE TABLE IF NOT EXISTS UND_SYSTEM_USER (
    ID_SYSTEM_USER INT PRIMARY KEY AUTO_INCREMENT,
    USERNAME VARCHAR(30) NOT NULL UNIQUE,
    PASSWORD VARCHAR(200) NOT NULL

);

CREATE TABLE IF NOT EXISTS UND_SYSTEM_ROLES (
    ID_SYSTEM_ROLES INT PRIMARY KEY AUTO_INCREMENT,
    NAME VARCHAR(20) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR(200)
);

CREATE TABLE IF NOT EXISTS UND_SYSTEM_USER_ROLES (
    ID_SYSTEM_USER INT NOT NULL,
    ID_SYSTEM_ROLES INT NOT NULL,

    FOREIGN KEY (ID_SYSTEM_USER)
    REFERENCES UND_SYSTEM_USER(ID_SYSTEM_USER),

    FOREIGN KEY (ID_SYSTEM_ROLES)
    REFERENCES UND_SYSTEM_ROLES(ID_SYSTEM_ROLES)
);


